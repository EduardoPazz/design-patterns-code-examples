class GarageDoor {
	void up() {
		System.out.println("Garage door up");
	}

	void down() {
		System.out.println("Garage door down");
	}

	void stop() {
		System.out.println("Garage door stopped");
	}

	void lightOn() {
		System.out.println("Garage light is on");
	}

	void lightOff() {
		System.out.println("Garage light is off");
	}
}
